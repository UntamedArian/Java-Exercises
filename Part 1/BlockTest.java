/* This is to showcase the functionality of block codes in Java

Filename would be BlockTest.java

*/

class BlockTest
{
    public static void main (String args[])
    {
        int x, y;
        y=20;
        for (x=0;x<10;x++)
        {
            System.out.println("This is X - " + x);
            System.out.println("This is y - " + y);
            y = y-2;
        }
    }
}